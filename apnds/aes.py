# 4x4 matrix.. we need it assignable
State = MutableSequence[MutableSequence[int]]

def key_expansion(key: bytes) -> bytes:
    round_key = bytearray(key) + bytearray(160)
    for i in range(4, 44):
        tmp = round_key[(i - 1) * 4:i * 4]
        if i & 3 == 0:
            tmp = bytearray(SBOX[c] for c in tmp[1:] + tmp[:1])
            tmp[0] ^= RCON[i >> 2]
        round_key[i * 4:(i + 1) * 4] = bytes(a ^ b for a, b in zip(round_key[(i - 4) * 4:(i - 3) * 4], tmp))
    return bytes(round_key)

def add_round_key(round: int, state: State, round_key: bytes) -> None:
    for i in range(4):
        for j in range(4):
            state[i][j] ^= round_key[16 * round + 4 * i + j]

def sub_bytes(state: State) -> None:
    for i in range(4):
        for j in range(4):
            state[j][i] = SBOX[state[j][i]]

def shift_rows(state: State) -> None:
    tmp = state[0][1]
    state[0][1] = state[1][1]
    state[1][1] = state[2][1]
    state[2][1] = state[3][1]
    state[3][1] = tmp

    tmp = state[0][2]
    state[0][2] = state[2][2]
    state[2][2] = tmp

    tmp = state[1][2]
    state[1][2] = state[3][2]
    state[3][2] = tmp

    tmp = state[0][3]
    state[0][3] = state[3][3]
    state[3][3] = state[2][3]
    state[2][3] = state[1][3]
    state[1][3] = tmp

def xtime(x: int) -> int:
    return ((x << 1) ^ (((x >> 7) & 1) * 0x1B)) & 0xFF

def mix_columns(state: State) -> None:
    for i in range(4):
        t = state[i][0]
        tmp = t ^ state[i][1] ^ state[i][2] ^ state[i][3]
        state[i][0] ^= tmp ^ xtime(t ^ state[i][1])
        state[i][1] ^= tmp ^ xtime(state[i][1] ^ state[i][2])
        state[i][2] ^= tmp ^ xtime(state[i][2] ^ state[i][3])
        state[i][3] ^= tmp ^ xtime(state[i][3] ^ t)

def cipher(state: State, round_key: bytes):
    add_round_key(0, state, round_key)

    round = 1
    while True:
        sub_bytes(state)
        shift_rows(state)
        if round == 10:
            break
        mix_columns(state)
        add_round_key(round, state, round_key)
        round += 1

    add_round_key(10, state, round_key)

def buf_to_state(buf: bytearray | bytes) -> State:
    return [list(buf[4 * i:4 * (i + 1)]) for i in range(4)]

def state_to_buf(state: State) -> bytes:
    return bytes(c for r in state for c in r)

def aes_ctr(key: bytes, iv: bytes, data: bytes, reverse_xor_mask: bool = False) -> bytes:
    round_key = key_expansion(key)
    buf = b''
    iv_m = bytearray(iv)

    def process_byte(i: int, c: int) -> int:
        nonlocal buf
        if i & 0xF == 0:
            state = buf_to_state(iv_m)
            cipher(state, round_key)
            buf = state_to_buf(state)
            if reverse_xor_mask:
                # modcrypt uses a slight variaton on the algorithm
                buf = buf[::-1]

            for bi in range(15, -1, -1):
                if iv_m[bi] == 255:
                    iv_m[bi] = 0
                    continue
                else:
                    iv_m[bi] += 1
                    break

        return c ^ buf[i & 0xF]

    return bytes(process_byte(*t) for t in enumerate(data))

